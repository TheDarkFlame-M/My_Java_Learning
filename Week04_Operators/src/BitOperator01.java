public class BitOperator01 {
    public static void main(String[] args) {
        // 按位取反
        // 2的三码 00000000 00000000 00000000 00000010
        // ~运算  11111111 11111111 11111111 11111101 // 补码
        // 取原码 <- 取反码 <- 负数补码-1
        // 得原码  10000000 00000000 00000000 00000011
        System.out.println(~2); // -3

        // 按位或
        // 2的补码 00000000 00000000 00000000 00000010
        // 3的补码 00000000 00000000 00000000 00000011
        // | 运算  00000000 00000000 00000000 00000011
        System.out.println(2 | 3); // 3

        // 按位异或
        // 2的补码 00000000 00000000 00000000 00000010
        // 3的补码 00000000 00000000 00000000 00000011
        // ^运算   00000000 00000000 00000000 00000001
        System.out.println(2 ^ 3); // 1

        System.out.println(~-2); // 1
    }
}