//演示四种进制
public class BinaryTest {
    public static void main(String[] args){
        int n1 = 0b1010; //二进制
        int n2 = 01010; //八进制
        int n3 = 1010; //十进制
        int n4 = 0X1010; //十六进制
        System.out.println("n1=" + n1);
        System.out.println("n2=" + n2);
        System.out.println("n3=" + n3);
        System.out.println("n4=" + n4);
    }
}